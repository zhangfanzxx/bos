package com.itheima.domain;

import javax.xml.bind.annotation.XmlRootElement;
import java.util.Date;

/**
 * @description:客户信息表
 * 
 */

@XmlRootElement(name = "customer")
public class Customer {
    private Long id; // 主键id
    private String username; // 用户名
    private String password; // 密码
    private Integer type; // 类型
    private Date birthday; // 生日
    private Integer sex; // 性别
    private String telephone; // 手机
    private String company; // 公司
    private String department; // 部门
    private String position; // 职位
    private String address; // 地址
    private String mobilePhone; // 座机
    private String email; // 邮箱
    private String fixedAreaId; // 定区编码. 逻辑外键

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

   
    public Integer getType() {
        return type;
    }

    public void setType(Integer type) {
        this.type = type;
    }

    public Date getBirthday() {
        return birthday;
    }

    public void setBirthday(Date birthday) {
        this.birthday = birthday;
    }

    public Integer getSex() {
        return sex;
    }

    public void setSex(Integer sex) {
        this.sex = sex;
    }

    public String getTelephone() {
        return telephone;
    }

    public void setTelephone(String telephone) {
        this.telephone = telephone;
    }

    public String getCompany() {
        return company;
    }

    public void setCompany(String company) {
        this.company = company;
    }

    public String getDepartment() {
        return department;
    }

    public void setDepartment(String department) {
        this.department = department;
    }

    public String getPosition() {
        return position;
    }

    public void setPosition(String position) {
        this.position = position;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getMobilePhone() {
        return mobilePhone;
    }

    public void setMobilePhone(String mobilePhone) {
        this.mobilePhone = mobilePhone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getFixedAreaId() {
        return fixedAreaId;
    }

    public void setFixedAreaId(String fixedAreaId) {
        this.fixedAreaId = fixedAreaId;
    }

    @Override
    public String toString() {
        return "Customer [id=" + id + ", username=" + username + ", password="
                + password + ", type=" + type + ", birthday=" + birthday
                + ", sex=" + sex + ", telephone=" + telephone + ", company="
                + company + ", department=" + department + ", position="
                + position + ", address=" + address + ", mobilePhone="
                + mobilePhone + ", email=" + email + ", fixedAreaId="
                + fixedAreaId + "]";
    }

}
